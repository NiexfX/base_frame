<template>
    <Form ref="filterForm" :model="filterForm" :label-width="100" label-colon>
        <Row>
            <Col v-bind="grid">
                <FormItem label="所属课题" prop="project_id">
                     <Select v-model="filterForm.project_id">
                        <Option v-for="item in projects" :key="item.id" :value="item.id">{{item.name}}</Option>
                    </Select>
                </FormItem>
            </Col>
            <Col v-bind="grid" >
                <Button class="common-left" type="primary" @click="handleFilter">查询</Button>
                <Button class="common-left" @click="handleReset">重置</Button>
            </Col>
        </Row> 
    </Form>
</template>

<script>
export default {
    data () {
        return {
            projects: [],
            grid: {
                xl: 6,
                lg: 8,
                md: 12,
                sm: 24,
                xs: 24
            },
            filterForm: {}
        }
    },
    async mounted () {
        let projectsRes= await this.$get('systemSelect', { target: 'project' })
        this.projects = projectsRes.data.data
    },
    methods: {
        handleFilter () {
            this.$emit('on-submit', this.filterForm)
        },
        handleReset () {
            this.$refs.filterForm.resetFields()
            this.$emit('on-reset')
        }
    }
}
</script>